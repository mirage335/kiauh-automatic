	set_nginx_permissions

	### symlink nginx log
	symlink_webui_nginx_log "mainsail"

	### add mainsail to the update manager in moonraker.conf
	patch_mainsail_update_manager

	fetch_webui_ports #WIP

	### confirm message
	print_confirm "Mainsail has been set up!"
}
